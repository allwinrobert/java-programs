import java.util.*;

public class pattern {

	public static void main(String[] args) {
			
	int a=5;
	for(int i=0;i<=a;i++) {
		for(int j=0;j<i;j++) {
			if(i%2!=0) {
			System.out.print("*");
			}
		}
		for(int j=0;j<a;j++) {
			System.out.println(" ");
		}
	}
	}
}
	

			


